# ==============================
# 🕸️ EEG → CONNECTIVITY GRAPH
# ==============================
def compute_connectivity(x, threshold=conn_threshold):
    """Builds a symmetric, GCN-normalized functional-connectivity adjacency
    matrix between EEG channels using Pearson correlation over the window.
    x: (C, T) channel-by-time array for a single window."""
    corr = np.corrcoef(x)                        # (C,C)
    corr = np.nan_to_num(corr)
    A = np.abs(corr)
    A[A < threshold] = 0.0
    np.fill_diagonal(A, 1.0)                      # self-loops
    D = A.sum(axis=1)
    D_inv_sqrt = np.diag(1.0/np.sqrt(D + 1e-6))
    A_norm = D_inv_sqrt @ A @ D_inv_sqrt           # Kipf & Welling normalization
    return A_norm.astype(np.float32)

# Pure PyTorch Mamba Block (same as Section 1 above, re-declared here so this
# section can be run standalone after a runtime restart)
class Mamba(nn.Module):
    def __init__(self, d_model, d_state=16, d_conv=4, expand=2):
        super().__init__()
        self.d_model = d_model
        self.d_state = d_state
        self.d_conv = d_conv
        self.expand = expand
        self.d_inner = d_model * expand
        self.dt_rank = math.ceil(self.d_model / 16)

        self.in_proj = nn.Linear(self.d_model, self.d_inner * 2, bias=False)

        self.conv1d = nn.Conv1d(
            in_channels=self.d_inner,
            out_channels=self.d_inner,
            kernel_size=self.d_conv,
            bias=True,
            groups=self.d_inner,
            padding=self.d_conv - 1
        )

        self.x_proj = nn.Linear(self.d_inner, self.dt_rank + self.d_state * 2, bias=False)

        self.dt_proj = nn.Linear(self.dt_rank, self.d_inner, bias=True)

        A = torch.arange(1, self.d_state + 1, dtype=torch.float32).repeat(self.d_inner, 1)
        self.A_log = nn.Parameter(torch.log(A))

        self.D = nn.Parameter(torch.ones(self.d_inner))

        self.out_proj = nn.Linear(self.d_inner, self.d_model, bias=False)

    def forward(self, x):
        B, L, _ = x.shape

        projected = self.in_proj(x)
        x, z = projected.chunk(2, dim=-1)

        x = x.transpose(-1, -2)
        x = self.conv1d(x)[:, :, :L]
        x = x.transpose(-1, -2)

        x = F.silu(x)

        x_dbl = self.x_proj(x)
        dt, B_ssm, C_ssm = torch.split(x_dbl, [self.dt_rank, self.d_state, self.d_state], dim=-1)

        dt = F.softplus(self.dt_proj(dt))

        A = -torch.exp(self.A_log.float())

        h = torch.zeros(B, self.d_inner, self.d_state, device=x.device, dtype=x.dtype)
        ys = []
        for l in range(L):
            dt_l = dt[:, l, :].unsqueeze(-1)
            B_l = B_ssm[:, l, :].unsqueeze(1)
            C_l = C_ssm[:, l, :].unsqueeze(1)
            x_l = x[:, l, :].unsqueeze(-1)

            dA = torch.exp(dt_l * A.unsqueeze(0))
            dB = dt_l @ B_l

            h = dA * h + dB * x_l

            y_l = (h @ C_l.transpose(-1, -2)).squeeze(-1)
            ys.append(y_l)

        y = torch.stack(ys, dim=1)

        y = y + x * self.D.unsqueeze(0).unsqueeze(0)

        y = y * F.silu(z)
        return self.out_proj(y)

# ==============================
# 🕸️ GRAPH CONVOLUTION LAYER
# ==============================
class GraphConvLayer(nn.Module):
    """Graph Convolution (Kipf & Welling propagation rule): H' = A_norm @ H @ W"""
    def __init__(self, in_features, out_features):
        super().__init__()
        self.linear = nn.Linear(in_features, out_features)

    def forward(self, x, A_norm):
        # x      : (B, T, N)   node signal sequence, N = EEG channels (graph nodes)
        # A_norm : (B, N, N)   normalized functional-connectivity adjacency
        x = torch.einsum('bnm,btm->btn', A_norm, x)   # spatial graph aggregation
        return self.linear(x)                          # learned node-wise transform

