# Pure PyTorch Mamba Block (Drop-in replacement for mamba_ssm.Mamba)
class Mamba(nn.Module):
    def __init__(self, d_model, d_state=16, d_conv=4, expand=2):
        super().__init__()
        self.d_model = d_model
        self.d_state = d_state
        self.d_conv = d_conv
        self.expand = expand
        self.d_inner = d_model * expand
        self.dt_rank = math.ceil(self.d_model / 16)
        
        # Input projection
        self.in_proj = nn.Linear(self.d_model, self.d_inner * 2, bias=False)
        
        # Causal Convolution
        self.conv1d = nn.Conv1d(
            in_channels=self.d_inner,
            out_channels=self.d_inner,
            kernel_size=self.d_conv,
            bias=True,
            groups=self.d_inner,
            padding=self.d_conv - 1
        )
        
        # x projection to dt, B, C
        self.x_proj = nn.Linear(self.d_inner, self.dt_rank + self.d_state * 2, bias=False)
        
        # dt projection
        self.dt_proj = nn.Linear(self.dt_rank, self.d_inner, bias=True)
        
        # SSM parameters
        # S4D initialization for A
        A = torch.arange(1, self.d_state + 1, dtype=torch.float32).repeat(self.d_inner, 1)
        self.A_log = nn.Parameter(torch.log(A))
        
        # D parameter (skip connection)
        self.D = nn.Parameter(torch.ones(self.d_inner))
        
        # Output projection
        self.out_proj = nn.Linear(self.d_inner, self.d_model, bias=False)

    def forward(self, x):
        # x: (B, L, d_model)
        B, L, _ = x.shape
        
        # Projection to x and z branches
        projected = self.in_proj(x)  # (B, L, d_inner * 2)
        x, z = projected.chunk(2, dim=-1)  # (B, L, d_inner), (B, L, d_inner)
        
        # Causal Conv1D
        x = x.transpose(-1, -2)  # (B, d_inner, L)
        x = self.conv1d(x)[:, :, :L]  # Causal convolution, keep length L
        x = x.transpose(-1, -2)  # (B, L, d_inner)
        
        x = F.silu(x)
        
        # Project x to dt, B, C
        x_dbl = self.x_proj(x)  # (B, L, dt_rank + d_state * 2)
        dt, B_ssm, C_ssm = torch.split(x_dbl, [self.dt_rank, self.d_state, self.d_state], dim=-1)
        
        # Discretize dt
        dt = F.softplus(self.dt_proj(dt))  # (B, L, d_inner)
        
        # Reconstruct A
        A = -torch.exp(self.A_log.float())  # (d_inner, d_state)
        
        # SSM Scan
        h = torch.zeros(B, self.d_inner, self.d_state, device=x.device, dtype=x.dtype)
        ys = []
        for l in range(L):
            dt_l = dt[:, l, :].unsqueeze(-1)  # (B, d_inner, 1)
            B_l = B_ssm[:, l, :].unsqueeze(1)    # (B, 1, d_state)
            C_l = C_ssm[:, l, :].unsqueeze(1)    # (B, 1, d_state)
            x_l = x[:, l, :].unsqueeze(-1)    # (B, d_inner, 1)
            
            # Discretize A and B
            dA = torch.exp(dt_l * A.unsqueeze(0))  # (B, d_inner, d_state)
            dB = dt_l @ B_l  # (B, d_inner, d_state)
            
            # State update
            h = dA * h + dB * x_l
            
            # Output step
            y_l = (h @ C_l.transpose(-1, -2)).squeeze(-1)  # (B, d_inner)
            ys.append(y_l)
            
        y = torch.stack(ys, dim=1)  # (B, L, d_inner)
        
        # Residual connection
        y = y + x * self.D.unsqueeze(0).unsqueeze(0)
        
        # Gate and out projection
        y = y * F.silu(z)
        return self.out_proj(y)

