"""## EEG **GRAPH MAMBA** (Connectivity-Graph + Mamba)

ye section EEG window ko ek **functional-connectivity graph** me convert karta
hai (64 channels = graph nodes, Pearson correlation = edges), fir us graph ko
ek **Graph Convolution (GCN)** se spatially encode karke, same Pure-PyTorch
`Mamba` block (Section 1 wala) se temporally model karta hai.

Pipeline (per window):
1. `compute_connectivity()` → (64,64) symmetric-normalized adjacency matrix `A`
   from the windowed, z-scored channel signals.
2. `GraphConvLayer` (×2) → spatially mixes channels using `A`, exactly like a
   standard GCN propagation rule `H' = A_norm @ H @ W`.
3. `Mamba` → models the resulting (256, d_model) sequence over time, same as
   the plain EEGMamba model above.
"""

