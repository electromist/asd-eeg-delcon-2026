class Simple1DCNN(nn.Module):
    def __init__(self, in_channels=64, num_classes=2):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv1d(in_channels, 32, kernel_size=7, padding=3),
            nn.ReLU(),
            nn.MaxPool1d(2),
            nn.Conv1d(32, 64, kernel_size=5, padding=2),
            nn.ReLU(),
            nn.MaxPool1d(2),
            nn.Conv1d(64, 64, kernel_size=3, padding=1),
            nn.ReLU(),
        )
        self.pool = nn.AdaptiveAvgPool1d(1)
        self.fc = nn.Linear(64, num_classes)

    def forward(self, x):
        # x: (B,64,256)
        x = self.net(x)
        x = self.pool(x).squeeze(-1)
        return self.fc(x)


class SimpleLSTM(nn.Module):
    def __init__(self, input_size=64, hidden_size=64, num_layers=2, num_classes=2):
        super().__init__()
        self.lstm = nn.LSTM(input_size, hidden_size, num_layers,
                             batch_first=True, dropout=0.2)
        self.fc = nn.Linear(hidden_size, num_classes)

    def forward(self, x):
        # (B,64,256) → (B,256,64)
        x = x.permute(0,2,1)
        out,_ = self.lstm(x)
        out = out.mean(dim=1)
        return self.fc(out)


class SimpleGRU(nn.Module):
    def __init__(self, input_size=64, hidden_size=64, num_layers=2, num_classes=2):
        super().__init__()
        self.gru = nn.GRU(input_size, hidden_size, num_layers,
                           batch_first=True, dropout=0.2)
        self.fc = nn.Linear(hidden_size, num_classes)

    def forward(self, x):
        x = x.permute(0,2,1)
        out,_ = self.gru(x)
        out = out.mean(dim=1)
        return self.fc(out)


class CNNLSTMHybrid(nn.Module):
    def __init__(self, in_channels=64, hidden_size=64, num_classes=2):
        super().__init__()
        self.cnn = nn.Sequential(
            nn.Conv1d(in_channels, 64, kernel_size=5, padding=2),
            nn.ReLU(),
            nn.MaxPool1d(2),
        )
        self.lstm = nn.LSTM(64, hidden_size, num_layers=1, batch_first=True)
        self.fc = nn.Linear(hidden_size, num_classes)

    def forward(self, x):
        # x: (B,64,256)
        x = self.cnn(x)            # (B,64,128)
        x = x.permute(0,2,1)       # (B,128,64)
        out,_ = self.lstm(x)
        out = out.mean(dim=1)
        return self.fc(out)

